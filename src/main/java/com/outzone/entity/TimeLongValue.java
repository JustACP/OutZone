package com.outzone.entity;

public class TimeLongValue {
    public static  final long Second = (long) 1000;
    public static  final long Minute = (long)1000 * 60;
    public static  final long Hour  = (long) 1000 * 60 * 60;
    public static  final long Day = (long) 1000 * 60* 60 * 24;
    public static  final long Month = (long) 1000 * 60* 60 * 24 * 30;
    public static  final long Year = Day * 365;

}
